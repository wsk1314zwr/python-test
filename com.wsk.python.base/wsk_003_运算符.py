"""
加减乘除
1. 对列表进行+=操作，本质是调用列表的extend方法
"""
print(2 + 1)
print(2 - 1)
print(2 * 1)
print(9 / 2)

# 取整
print(9 // 2)

# 取余
print(9 % 2)

# 次方计算
print(9 ** 2)

# 与字符串的乘法
print("1" * 9)

print("-----------非数字类型的 运算符--------------")
print([1, 2, 3] * 5)
print([1, 2, 3] + [1, 7])
print(4 in [1, 2, 4])
print("a" not in "ada")
print("a" not in {"v": "ss"})
